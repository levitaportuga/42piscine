void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	f;
	int	swap;

	f = 0;
	while (f < size)
	{
		i = 0;
		while (i < size - 1 - f)
		{
			if (tab[i] > tab[i + 1])
			{
				swap = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = swap;
			}
			i++;
		}
		f++;
	}
}
