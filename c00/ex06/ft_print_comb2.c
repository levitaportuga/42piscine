#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	i[5];

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			i[0] = a / 10 + '0';
			i[1] = a % 10 + '0';
			i[2] = ' ';
			i[3] = b / 10 + '0';
			i[4] = b % 10 + '0';
			write(1, i, 5);
			if (a <= 97 && b <= 99)
			{
				write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
